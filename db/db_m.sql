-- Pulizia del database (DROP in ordine inverso di dipendenza)
DROP TABLE IF EXISTS public.messaggi CASCADE;
DROP TABLE IF EXISTS public.utenti CASCADE;
DROP TABLE IF EXISTS public.giocatori CASCADE;

-------------------------------------------------------
-- 1. TABELLA GIOCATORI
-------------------------------------------------------
CREATE TABLE public.giocatori (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome character varying(100) NOT NULL,
    nascita character varying(20),
    ruolo character varying(3),
    gol integer DEFAULT 0,
    presenze integer DEFAULT 0,
    ammonizioni integer DEFAULT 0,
    espulsioni integer DEFAULT 0,
    CONSTRAINT giocatori_ruolo_check CHECK (ruolo IN ('POR', 'DIF', 'CEN', 'ATT'))
);

-------------------------------------------------------
-- 2. TABELLA UTENTI
-------------------------------------------------------
CREATE TABLE public.utenti (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome character varying(50) NOT NULL,
    email character varying(100) UNIQUE NOT NULL,
    password character varying(255) NOT NULL,
    giocatore_preferito character varying(50),
    ruolo character varying(20) DEFAULT 'tifoso'
);

-------------------------------------------------------
-- 3. TABELLA MESSAGGI (Dipende da Utenti)
-------------------------------------------------------
CREATE TABLE public.messaggi (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_utente integer REFERENCES public.utenti(id) ON DELETE CASCADE,
    testo text NOT NULL,
    data_invio timestamp without time zone DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE public.partite (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    giornata integer NOT NULL,
    casa character varying(100) NOT NULL,
    ospite character varying(100) NOT NULL,
    data_match timestamp without time zone NOT NULL,
    giocata boolean DEFAULT false,
    risultato_casa integer,
    risultato_ospite integer
);

-- Creazione tabella classifica
CREATE TABLE public.classifica (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    squadra character varying(100) NOT NULL,
    pt integer DEFAULT 0,
    g integer DEFAULT 0,
    v integer DEFAULT 0,
    n integer DEFAULT 0,
    p integer DEFAULT 0,
    dr integer DEFAULT 0,
    is_morra boolean DEFAULT false
);

-- Correzione nomi colonne in partite (per allinearsi al tuo PHP)
ALTER TABLE public.partite RENAME COLUMN risultato_casa TO gol_casa;
ALTER TABLE public.partite RENAME COLUMN risultato_ospite TO gol_ospite;

-- Tabella per definire il modulo tattico di una partita
CREATE TABLE public.formazioni_setup (
    id_partita integer PRIMARY KEY REFERENCES public.partite(id) ON DELETE CASCADE,
    modulo character varying(10) DEFAULT '4-4-2'
);

-- Tabella per i titolari in campo (posizione X,Y sul campo)
CREATE TABLE public.formazione_giocatori (
    id_partita integer REFERENCES public.partite(id) ON DELETE CASCADE,
    id_giocatore integer REFERENCES public.giocatori(id) ON DELETE CASCADE,
    linea integer NOT NULL, -- 1: POR, 2: DIF, 3: CEN, 4: TRQ, 5: ATT
    ordine_orizzontale integer NOT NULL, -- Da sinistra a destra (1, 2, 3...)
    PRIMARY KEY (id_partita, id_giocatore)
);

-- Tabella per i giocatori disponibili (panchina)
CREATE TABLE public.panchina (
    id_partita integer REFERENCES public.partite(id) ON DELETE CASCADE,
    id_giocatore integer REFERENCES public.giocatori(id) ON DELETE CASCADE,
    PRIMARY KEY (id_partita, id_giocatore)
);

-- Tabella per le sostituzioni effettuate
CREATE TABLE public.sostituzioni (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_partita integer REFERENCES public.partite(id) ON DELETE CASCADE,
    id_entra integer REFERENCES public.giocatori(id),
    id_esce integer REFERENCES public.giocatori(id)
);

CREATE TABLE public.cronaca_live (
    id SERIAL PRIMARY KEY,
    id_partita INTEGER REFERENCES public.partite(id) ON DELETE CASCADE,
    minuto VARCHAR(10) NOT NULL,
    tipo_evento VARCHAR(20) NOT NULL,
    testo TEXT NOT NULL,
    data_inserimento TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

